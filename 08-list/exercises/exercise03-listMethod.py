# Given initial list
my_list = [3, 7, 12, 5, 9]

# Use append() to add the number 15
my_list.append(15)

# Use insert() to insert the number 20 at index 2
my_list.insert(2, 20)

# Use remove() to remove the number 5
my_list.remove(5)

# Use pop() to remove and return the last element
last_element = my_list.pop()

# Use index() to find the index of the element 7
index_of_7 = my_list.index(7)

# Use sort() to sort the list in ascending order
my_list.sort()

# Print the modified list and other results
print("Modified List:", my_list)
print("Removed and Returned Last Element:", last_element)
print("Index of Element 7:", index_of_7)
